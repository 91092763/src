package non_tkp.mr_c_challenges;

public class Beginner05Print100Integers1by100
{
  public static void main(String[] args)
  {
    for (int i = 1; i < 100; i++)
    {
      System.out.print(i + "");
    }
    System.out.println();
    System.out.println();
    for (int i = 1; i < 101; i++)
    {
      System.out.print(i + "");
    }
  }
}
